                            <h3>Facts:</h3>
                            <ul class="list-group">
                                <li class="list-group-item">The current members of Metallica are James Hetfield, Lars Ulrich, Kirk Hammett, and Robert Trujillo.
</li>
                                <li class="list-group-item">James Hetfield and Lars Ulrich started Metallica.</li>
                                <li class="list-group-item">Metallica’s longest song is 9 minutes and 57 seconds long, and is entitled “Suicide & Redemption.”</li>
                                <li class="list-group-item">Metallica’s shortest song is 3 minutes and 8 seconds long, and is entitled “Motorbreath.”</li>
                                <li class="list-group-item">Dave Mustaine was Metallica’s first lead guitarist.</li>
                                <li class="list-group-item">In total, Metallica has had 8 members that have either came and gone, or are still in the band.</li>
                                <li class="list-group-item">Metallica’s Black Album has sold 15,620,000 units.</li>
                                <li class="list-group-item">Metallica have had 5 of their records hit the number one spot on the billboard 100 charts.</li>
                                <li class="list-group-item">Metallica have made 10 full length albums.</li>
                                <li class="list-group-item">Metallica have played 1,724 concerts in the last 30 years.</li>
                                <li class="list-group-item">That means they play around 57.5 concerts per year.</li>
                                <li class="list-group-item">Metallica have played in San Francisco more than any other city.</li>
                                <li class="list-group-item">Metallica have played in 60 different countries.</li>
                                <li class="list-group-item">Metallica have played their song “Master of Puppets” live 1,256 times.</li>
                                <li class="list-group-item">Metallica’s old bass player Cliff Burton was killed on September 27th, 1986 when Metallica’s tour bus flipped over in Sweden while they were on tour.</li>
                                <li class="list-group-item">Metallica have won 14 Grammy awards.</li>
                                <li class="list-group-item">Metallica’s guitarist, Kirk Hammett, is a vegetarian.</li>
                                <li class="list-group-item">Metallica’s drummer, Lars Ulrich, collects a lot of art.</li>
                                <li class="list-group-item">James Hetfield learned to play piano before the guitar.</li>
                                <li class="list-group-item">Robert Trujillo has a side project outside of Metallica called Mass Mental.</li>
                                <li class="list-group-item">It is said that Metallica spent $400,000 on their music video for their song “The Memory Remains.”</li>
                                <li class="list-group-item">The first band Cliff Burton made EZ-Street.</li>
                                <li class="list-group-item">Lars Ulrich and James Hetfield voiced on the disney show “Dave The Barbarian,” for one episode.</li>
                                <li class="list-group-item">In 1984, Metallica’s “Ride The Lightning” was pressed on a misprinted cover, with the glowing outline being green instead of the first intended blue. There are only 400 copies of the green outlined misprint in existence.</li>
                                <li class="list-group-item">Darren Aronofsky, an award-winning actor made the music video for the Metallica song “The View.”</li>
                                <li class="list-group-item">The first band James Hetfield was in was called “Obsession.”</li>
                                <li class="list-group-item">Metallica cancelled a show in Evansville, Indiana when James Hetfield broke his wrist while skateboarding.</li>
                                <li class="list-group-item">For 2 months, Metal Church’s John Marshall took over for Hetfield when he had his broken wrist.</li>
                                <li class="list-group-item">Lars Ulrich played drums for the Mercyful Fate song “Return of the Vampire.”</li>
                                <li class="list-group-item">Lars Ulrich sold a painting from his collection of paintings for $13,500,000.</li>
                                <li class="list-group-item">Cliff Burton came up with the album name “Kill ‘Em All.”</li>
                                <li class="list-group-item">Dave Mustaine was kicked out of Metallica a month before the recording session of the album “Kill ‘Em All.”</li>
                                <li class="list-group-item">Kirk Hammett had to re-work Dave’s guitar solos on the album “Kill ‘Em All.”</li>
                                <li class="list-group-item">Some songs were songs various Metallica members had made with their previous bands.</li>
                                <li class="list-group-item">“Seek And Destroy,” the song by Metallica, was once used as a wrestling entrance song.</li>
                                <li class="list-group-item">Metallica has sold over 3,000,000 copies of their record “Kill ‘Em All.”</li>
                                <li class="list-group-item">Metallica has ended their setlist at concerts with their song “Seek and Destroy” almost every time they’ve played since 2003.</li>
                                <li class="list-group-item">There are shoes with the album art for “Kill ‘Em All” on it.</li>
                                <li class="list-group-item">In March 1986, Metallica’s album “Master of Puppets” reached number 41 on the UK most popular album charts.</li>
                                <li class="list-group-item">Metallica’s album “Kill ‘Em All” was released in the summer of 1983.</li
